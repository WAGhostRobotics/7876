void servoUp()
{
	servo[jack1] = 225;
	servo[jack2] = 0;
}
void servoDown()
{
	servo[jack1] = 0;
	servo[jack2] = 225;
}


void raiseLift()
{
	ClearTimer(T1);
	while(abs(nMotorEncoder[elevator]) < 8400 && time1[T1] <2500)
	{
		motor[elevator] = 100;
		print(nMotorEncoder[elevator],4);
		PlaySound(soundBlip);
	}
	motor[elevator] = 0;
}

void lowerLift()
{
	ClearTimer(T1);
	while(abs(nMotorEncoder[elevator]) > 00 && time1[T1] <2500)
	{
		motor[elevator] = -100;
		print(nMotorEncoder[elevator],4);
		PlaySound(soundBlip);
	}
	motor[elevator] = 0;
}

void dump()
{
	servo[bucket] = 0;
}

void dumpUp()
{
	servo[bucket] = 225;

}
void initializeRobot()
{
	servo[bucket] = 225;
	servo[jack1] = 225;
	servo[jack2] = 0;

}

