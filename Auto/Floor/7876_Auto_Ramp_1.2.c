/*
Attach to the 60 cm rolling goal and dump the autonomous balls
*/
void attachAndDump()
{
	stopMotors();
	wait1Msec(500);
	servoDown();
	wait1Msec(500);
	driveInches(-5);
	raiseLift();
	wait1Msec(500);
	dump();
	wait1Msec(800);
	dumpUp();
	wait1Msec(500);
}

/**
Turn and drive towards the parking zone
**/
void moveToParkingZone()
{
	//Turn to angle back of robot towards parking zone
	nMotorEncoder[left] = 0;
	nMotorEncoder[right] = 0;
	while(abs(nMotorEncoder[left]) < inchesToEncoder(4.2))
	{
		motor[left]= 50;
		motor[right] = -50;
		print(nMotorEncoder[left], 4);
	}


	stopMotors();
	//drive backwards to parking zone
	driveInches(-100,50,50);

}

/**
Release the goal in the parking zone
**/
void parkGoal()
{
	//~180 turn so that rolling goal faces parking zone
	turn(LEFT,250,100);
	//drive forward, pushing goal into parking zone
	driveInches(10,50,50);
	//release rolling goal
	servoUp();
}

/*
Back away from the parking zone and get ready for the teleop phase
*/
void postParkGoal()
{
	driveInches(-8,50,50);
	turn(RIGHT,8,100);
	driveInches(-30,100,100);
	turn(RIGHT, 12,100);
	lowerLift();
	turn(LEFT,100,100);
}

task main()
{
	preMovement();
	moveToFirstTube();
	attachAndDump();
	moveToParkingZone();
	parkGoal();
	postParkGoal();
}

