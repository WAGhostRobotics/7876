int inputWaitSecs() {
	int waitSecs = 0;
	eraseDisplay();
	print(waitSecs);
	while(nNxtButtonPressed != 3) {
		if(nNxtButtonPressed == 1) { //Right arrow
			waitSecs++;
			PlaySound(soundBlip);
			eraseDisplay();
			print(waitSecs);
			wait1Msec(500);
		}
		else if(nNxtButtonPressed == 2) {
			if(waitSecs > 0)
			{
				waitSecs--;
			}
			PlaySound(soundBlip);
			eraseDisplay();
			print(waitSecs);
			wait1Msec(500);
		}
	}
	PlaySound(soundBlip);
	return waitSecs;
}
